const express = require('express');
const cors = require('cors');
const morgan = require('morgan');
require('dotenv').config(); // Permet de charger les variables d'environnement depuis .env
const cookieParser = require('cookie-parser');

// Connexion à la base de donnée
const db = require('./db');
const {methods} = require("express/lib/utils");

//====Importation des routes ===
const produitRoutes = require('./produit/routes/ProduitRouter');
const clientRoutes = require('./client/routes/clientRouter');
const orderRoutes = require('./order/routes/OrderRouter');


// Création de l'application Express
const app = express();

// MIDDLEWARES
// Parser les JSON
app.use(express.json());


// Logger de requêtes HTTP dans la console
app.use(morgan('dev'));

// Sert les fichiers statiques (images, produits)
app.use(express.static('public'));


// Configurer Cors = permet les requêtes cross-origin qui viennent du front
// CORS = Cross-Origin Ressource Sharing
// OBLIGATOIRE  sinon le navigateur bloque les requêtes
app.use(
    cors({
    origin: process.env.FRONTEND_URL || "http://localhost:5173",
        methods: ["GET","PUT","POST","DELETE"],
        credentials: true, // Utiliser les cookies cross-origin (2 applications qui communiquent entre elles)
}),
);

// Parse les cookies dans req
app.use (cookieParser());


// ROUTES

// Route de test pour vérifier que l'api fonctionne
app.get("/health", (req, res) => {
    res.json({
        status: "OK",
        message: "API fonctionnelle",
    });
});


// Routes de l'API
app.use("/api/produits", produitRoutes);
app.use("/api/clients", clientRoutes);
app.use("/api/orders", orderRoutes);

// GESTIONS DES ERREURS
// Routes 404
app.use((req, res) => {
    res.status(404).json({
        message: "Route non trouvée",
    });
});


// DÉMARRAGE DU SERVEUR
const port = process.env.PORT || 3000;
const host = process.env.HOST || "localhost";

app.listen(port, host, () => {
    console.log(`Serveur démarré sur http://${host}:${port}`);
});
